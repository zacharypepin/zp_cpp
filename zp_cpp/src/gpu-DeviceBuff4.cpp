// ====================================================================================================================
// ====================================================================================================================
// ====================================================================================================================
// ====================================================================================================================
size_t zp::gpu::DeviceBuff4::size()
{
    return max_count * stride;
}

// ====================================================================================================================
// ====================================================================================================================
// ====================================================================================================================
// ====================================================================================================================
VkDescriptorBufferInfo zp::gpu::DeviceBuff4::desc_info()
{
    VkDescriptorBufferInfo desc_info = {};
    desc_info.buffer                 = handle;
    desc_info.offset                 = 0;
    desc_info.range                  = size();

    return desc_info;
}

// ====================================================================================================================
// ====================================================================================================================
// ====================================================================================================================
// ====================================================================================================================
void zp::gpu::DeviceBuff4::reset()
{
    count = 0;
}

// ====================================================================================================================
// ====================================================================================================================
// ====================================================================================================================
// ====================================================================================================================
void zp::gpu::DeviceBuff4::cleanup(VkDevice device)
{
    reset();
    vkUnmapMemory(device, memory);
    vkDestroyBuffer(device, handle, nullptr);
    vkFreeMemory(device, memory, nullptr);
}